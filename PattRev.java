public class PattRev
{
  public static void main (String[]args)
  {
        for(int i = 10; i >= 1; i--)
        {
            for(int j = 0; j <= i; j++)
            {
                System.out.print("+ " + " ");
            }
            System.out.println();
        }
        for (int k = 0; k <= 10; k++)
        {
            for(int a = 0; a <= k; a++)
            {
                System.out.print("+ " + " ");
            }
            System.out.println();
        }
  }
}
